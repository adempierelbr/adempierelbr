package org.adempierelbr.nfe.beans;

public class TransporteGrupoBean {
	
	private String transporta;
	private String CNPJ;
	private String CPF;
	private String xNome;
	private String IE;
	private String xEnder;
	private String xMun;
	private String UF;
	
	public String getTransporta() {
		return transporta;
	}
	public void setTransporta(String transporta) {
		this.transporta = transporta;
	}
	public String getCNPJ() {
		return CNPJ;
	}
	public void setCNPJ(String cnpj) {
		CNPJ = cnpj;
	}
	public String getCPF() {
		return CPF;
	}
	public void setCPF(String cpf) {
		CPF = cpf;
	}
	public String getXNome() {
		return xNome;
	}
	public void setXNome(String nome) {
		xNome = nome;
	}
	public String getIE() {
		return IE;
	}
	public void setIE(String ie) {
		IE = ie;
	}
	public String getXEnder() {
		return xEnder;
	}
	public void setXEnder(String ender) {
		xEnder = ender;
	}
	public String getXMun() {
		return xMun;
	}
	public void setXMun(String mun) {
		xMun = mun;
	}
	public String getUF() {
		return UF;
	}
	public void setUF(String uf) {
		UF = uf;
	}
	
}
