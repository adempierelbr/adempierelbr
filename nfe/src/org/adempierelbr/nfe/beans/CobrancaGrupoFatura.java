package org.adempierelbr.nfe.beans;

public class CobrancaGrupoFatura {

	private String nFat;
	private String vOrig;
	private String vDesc;
	private String vLiq;
	public String getNFat() {
		return nFat;
	}
	public void setNFat(String fat) {
		nFat = fat;
	}
	public String getVOrig() {
		return vOrig;
	}
	public void setVOrig(String orig) {
		vOrig = orig;
	}
	public String getVDesc() {
		return vDesc;
	}
	public void setVDesc(String desc) {
		vDesc = desc;
	}
	public String getVLiq() {
		return vLiq;
	}
	public void setVLiq(String liq) {
		vLiq = liq;
	}

}
