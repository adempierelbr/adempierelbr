package org.adempierelbr.nfe.beans;

public class TransporteVolBean {

	private String qVol;
	private String esp;
	private String marca;
	private String nVol;
	private String pesoL;
	private String pesoB;
	
	
	public String getQVol() {
		return qVol;
	}
	public void setQVol(String vol) {
		qVol = vol;
	}
	public String getEsp() {
		return esp;
	}
	public void setEsp(String esp) {
		this.esp = esp;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getNVol() {
		return nVol;
	}
	public void setNVol(String vol) {
		nVol = vol;
	}
	public String getPesoL() {
		return pesoL;
	}
	public void setPesoL(String pesoL) {
		this.pesoL = pesoL;
	}
	public String getPesoB() {
		return pesoB;
	}
	public void setPesoB(String pesoB) {
		this.pesoB = pesoB;
	}
	
	
	
	
}
